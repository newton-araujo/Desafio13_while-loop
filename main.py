"""

Desafio 13 - While Loop

Para este desafio, vamos usar o while loop para realizar um contador de 1 a 10. 

Requisito:
    - Não usar o for loop

"""
#Contador iniciando com 0.
contador = 0

#While loop: Iniciando 0 e vai até 10. Assim que o contador atingir 10 o loop será o final do loop

while contador != 10:
    contador += 1
    print(contador, end=' ')
